-- ---------------------------------------------------------------------------
-- Types & tables
-- ---------------------------------------------------------------------------
CREATE TYPE public.challenge_difficulty AS ENUM ('easy', 'medium', 'hard');

CREATE TABLE public.community_challenge_templates (
  id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  difficulty challenge_difficulty NOT NULL,
  points integer NOT NULL CHECK (points > 0),
  title text NOT NULL,
  description text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE public.community_daily_challenges (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  challenge_date date NOT NULL,
  difficulty challenge_difficulty NOT NULL,
  points integer NOT NULL CHECK (points > 0),
  title text NOT NULL,
  description text,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (challenge_date, difficulty)
);

CREATE TABLE public.user_challenge_completions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  challenge_id uuid NOT NULL REFERENCES public.community_daily_challenges (id) ON DELETE CASCADE,
  points_earned integer NOT NULL CHECK (points_earned > 0),
  completed_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (user_id, challenge_id)
);

CREATE TABLE public.community_comments (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  challenge_id uuid NOT NULL REFERENCES public.community_daily_challenges (id) ON DELETE CASCADE,
  content text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE public.community_reactions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  challenge_id uuid NOT NULL REFERENCES public.community_daily_challenges (id) ON DELETE CASCADE,
  reaction_type text NOT NULL DEFAULT 'like',
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (user_id, challenge_id, reaction_type)
);

CREATE INDEX idx_completions_user ON public.user_challenge_completions (user_id);
CREATE INDEX idx_completions_completed_at ON public.user_challenge_completions (completed_at);
CREATE INDEX idx_daily_challenges_date ON public.community_daily_challenges (challenge_date);
CREATE INDEX idx_community_comments_challenge ON public.community_comments (challenge_id);
CREATE INDEX idx_community_reactions_challenge ON public.community_reactions (challenge_id);

