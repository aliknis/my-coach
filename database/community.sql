-- ---------------------------------------------------------------------------
-- Leaderboard (period: week | month | all | all-time)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_community_leaderboard (p_period text)
RETURNS TABLE (
  rank bigint,
  user_id uuid,
  display_name text,
  avatar_url text,
  total_points bigint
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  WITH bounds AS (
    SELECT CASE lower(trim(p_period))
      WHEN 'week' THEN date_trunc('week', timezone('utc', now ()))
      WHEN 'month' THEN date_trunc('month', timezone('utc', now ()))
      WHEN 'all' THEN timestamptz '1970-01-01 00:00:00+00'
      WHEN 'all-time' THEN timestamptz '1970-01-01 00:00:00+00'
      ELSE date_trunc('week', timezone('utc', now ()))
    END AS start_ts
  ),
  scoring AS (
    SELECT
      c.user_id,
      sum(c.points_earned)::bigint AS pts
    FROM public.user_challenge_completions c
    CROSS JOIN bounds b
    WHERE c.completed_at >= b.start_ts
    GROUP BY c.user_id
    HAVING sum(c.points_earned) > 0
  )
  SELECT
    row_number() OVER (
      ORDER BY
        s.pts DESC,
        s.user_id
    )::bigint AS rank,
    s.user_id,
    coalesce(
      nullif(trim(u.username), ''),
      nullif(trim(concat_ws(' ', u.first_name, u.last_name)), ''),
      'Member'
    ) AS display_name,
    u.avatar_url,
    s.pts AS total_points
  FROM scoring s
  JOIN public.users u ON u.id = s.user_id
  WHERE u.is_active = true
  ORDER BY s.pts DESC, s.user_id
  LIMIT 100;
$$;

-- ---------------------------------------------------------------------------
-- Community stats (aggregates)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_community_stats ()
RETURNS json
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    json_build_object(
      'total_members', (
        SELECT count(*)::integer
        FROM public.users
        WHERE is_active = true
      ),
      'challenges_completed', (
        SELECT count(*)::integer
        FROM public.user_challenge_completions
      ),
      'active_this_week', (
        SELECT count(DISTINCT c.user_id)::integer
        FROM public.user_challenge_completions c
        WHERE c.completed_at >= date_trunc('week', timezone('utc', now ()))
      ),
      'points_distributed', (
        SELECT coalesce(sum(c.points_earned), 0)::bigint
        FROM public.user_challenge_completions c
      )
    );
$$;

-- ---------------------------------------------------------------------------
-- Recent activity feed
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_recent_community_achievements (p_limit integer DEFAULT 9)
RETURNS TABLE (
  display_name text,
  challenge_title text,
  points_earned integer,
  completed_at timestamptz
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    coalesce(
      nullif(trim(u.username), ''),
      nullif(trim(concat_ws(' ', u.first_name, u.last_name)), ''),
      'Member'
    ) AS display_name,
    dc.title AS challenge_title,
    c.points_earned,
    c.completed_at
  FROM public.user_challenge_completions c
  JOIN public.users u ON u.id = c.user_id
  JOIN public.community_daily_challenges dc ON dc.id = c.challenge_id
  WHERE u.is_active = true
  ORDER BY c.completed_at DESC
  LIMIT greatest(1, least(p_limit, 50));
$$;

-- ---------------------------------------------------------------------------
-- Streak: consecutive UTC days with at least one completion (anchor today or yesterday)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.community_streak_for_user (p_user_id uuid)
RETURNS integer
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  cur date := (timezone('utc', now ()))::date;
  d date;
  streak_val integer := 0;
BEGIN
  IF EXISTS (
    SELECT 1
    FROM public.user_challenge_completions x
    WHERE x.user_id = p_user_id
      AND (x.completed_at AT TIME ZONE 'UTC')::date = cur
  ) THEN
    d := cur;
  ELSIF EXISTS (
    SELECT 1
    FROM public.user_challenge_completions x
    WHERE x.user_id = p_user_id
      AND (x.completed_at AT TIME ZONE 'UTC')::date = cur - 1
  ) THEN
    d := cur - 1;
  ELSE
    RETURN 0;
  END IF;

  LOOP
    EXIT WHEN NOT EXISTS (
      SELECT 1
      FROM public.user_challenge_completions x
      WHERE x.user_id = p_user_id
        AND (x.completed_at AT TIME ZONE 'UTC')::date = d
    );
    streak_val := streak_val + 1;
    d := d - 1;
  END LOOP;

  RETURN streak_val;
END;
$$;

-- ---------------------------------------------------------------------------
-- Logged-in user summary (ranks among active users; 0 points included)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_my_community_stats ()
RETURNS json
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  uid uuid := auth.uid ();
  pts_all bigint;
  pts_week bigint;
  pts_month bigint;
  r_all bigint;
  r_week bigint;
  r_month bigint;
  str integer;
BEGIN
  IF uid IS NULL THEN
    RETURN json_build_object('logged_in', false);
  END IF;

  SELECT coalesce(sum(c.points_earned), 0) INTO pts_all
  FROM public.user_challenge_completions c
  WHERE c.user_id = uid;

  SELECT coalesce(sum(c.points_earned), 0) INTO pts_week
  FROM public.user_challenge_completions c
  WHERE c.user_id = uid
    AND c.completed_at >= date_trunc('week', timezone('utc', now ()));

  SELECT coalesce(sum(c.points_earned), 0) INTO pts_month
  FROM public.user_challenge_completions c
  WHERE c.user_id = uid
    AND c.completed_at >= date_trunc('month', timezone('utc', now ()));

  WITH scoring_all AS (
    SELECT
      u.id AS user_id,
      coalesce(sum(c.points_earned), 0)::bigint AS pts
    FROM public.users u
    LEFT JOIN public.user_challenge_completions c ON c.user_id = u.id
    WHERE u.is_active = true
    GROUP BY u.id
  ),
  ranked_all AS (
    SELECT
      user_id,
      rank () OVER (
        ORDER BY
          pts DESC,
          user_id
      ) AS rnk
    FROM scoring_all
  )
  SELECT rnk INTO r_all
  FROM ranked_all
  WHERE user_id = uid;

  WITH week_start AS (
    SELECT date_trunc('week', timezone('utc', now ())) AS ts
  ),
  scoring_week AS (
    SELECT
      u.id AS user_id,
      coalesce(
        sum(
          CASE
            WHEN c.completed_at >= (SELECT ts FROM week_start) THEN c.points_earned
          END
        ),
        0
      )::bigint AS pts
    FROM public.users u
    LEFT JOIN public.user_challenge_completions c ON c.user_id = u.id
    WHERE u.is_active = true
    GROUP BY u.id
  ),
  ranked_week AS (
    SELECT
      user_id,
      rank () OVER (
        ORDER BY
          pts DESC,
          user_id
      ) AS rnk
    FROM scoring_week
  )
  SELECT rnk INTO r_week
  FROM ranked_week
  WHERE user_id = uid;

  WITH month_start AS (
    SELECT date_trunc('month', timezone('utc', now ())) AS ts
  ),
  scoring_month AS (
    SELECT
      u.id AS user_id,
      coalesce(
        sum(
          CASE
            WHEN c.completed_at >= (SELECT ts FROM month_start) THEN c.points_earned
          END
        ),
        0
      )::bigint AS pts
    FROM public.users u
    LEFT JOIN public.user_challenge_completions c ON c.user_id = u.id
    WHERE u.is_active = true
    GROUP BY u.id
  ),
  ranked_month AS (
    SELECT
      user_id,
      rank () OVER (
        ORDER BY
          pts DESC,
          user_id
      ) AS rnk
    FROM scoring_month
  )
  SELECT rnk INTO r_month
  FROM ranked_month
  WHERE user_id = uid;

  SELECT public.community_streak_for_user (uid) INTO str;

  RETURN json_build_object(
    'logged_in', true,
    'rank_all', r_all,
    'rank_week', r_week,
    'rank_month', r_month,
    'points_all', pts_all,
    'points_week', pts_week,
    'points_month', pts_month,
    'streak', str
  );
END;
$$;

