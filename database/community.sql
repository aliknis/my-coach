-- ---------------------------------------------------------------------------
-- Ensure 3 global challenges per calendar day (UTC) from templates
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.ensure_community_daily_challenges (p_date date)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  diff challenge_difficulty;
  cnt integer;
  picked_id bigint;
BEGIN
  FOR diff IN SELECT unnest(
    ARRAY['easy', 'medium', 'hard']::challenge_difficulty[]
  )
  LOOP
    IF EXISTS (
      SELECT 1
      FROM public.community_daily_challenges d
      WHERE d.challenge_date = p_date
        AND d.difficulty = diff
    ) THEN
      CONTINUE;
    END IF;

    SELECT count(*)::integer INTO cnt
    FROM public.community_challenge_templates t
    WHERE t.difficulty = diff;

    IF cnt = 0 THEN
      RAISE EXCEPTION 'No community_challenge_templates for difficulty %', diff;
    END IF;

    SELECT t.id INTO picked_id
    FROM public.community_challenge_templates t
    WHERE t.difficulty = diff
    ORDER BY t.id
    OFFSET (abs(hashtext(p_date::text || diff::text)) % cnt)
    LIMIT 1;

    INSERT INTO public.community_daily_challenges (
      challenge_date,
      difficulty,
      points,
      title,
      description
    )
    SELECT
      p_date,
      t.difficulty,
      t.points,
      t.title,
      t.description
    FROM public.community_challenge_templates t
    WHERE t.id = picked_id;
  END LOOP;
END;
$$;

-- ---------------------------------------------------------------------------
-- Insert completion: enforce auth, same-day challenge, points from row
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.enforce_challenge_completion ()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  pts integer;
BEGIN
  NEW.user_id := auth.uid();
  IF NEW.user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  SELECT c.points INTO pts
  FROM public.community_daily_challenges c
  WHERE c.id = NEW.challenge_id
    AND c.challenge_date = (timezone('utc', now()))::date;

  IF pts IS NULL THEN
    RAISE EXCEPTION 'Invalid or expired challenge';
  END IF;

  NEW.points_earned := pts;
  NEW.completed_at := COALESCE(NEW.completed_at, now());
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_enforce_challenge_completion
  BEFORE INSERT ON public.user_challenge_completions
  FOR EACH ROW
  EXECUTE FUNCTION public.enforce_challenge_completion ();

-- ---------------------------------------------------------------------------
-- Sync user total_points
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.sync_user_total_points ()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    UPDATE public.users
    SET total_points = total_points + NEW.points_earned
    WHERE id = NEW.user_id;
  ELSIF (TG_OP = 'DELETE') THEN
    UPDATE public.users
    SET total_points = total_points - OLD.points_earned
    WHERE id = OLD.user_id;
  END IF;
  RETURN NULL;
END;
$$;

CREATE TRIGGER trg_sync_user_total_points
  AFTER INSERT OR DELETE ON public.user_challenge_completions
  FOR EACH ROW
  EXECUTE FUNCTION public.sync_user_total_points ();

